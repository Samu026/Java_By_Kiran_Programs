public class BitwiseOperator 
{
	public static void main(String args[])
	{
		//Bitwise Operators
		int a=5,b=9;
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(~a);
		System.out.println(~b);
		System.out.println(a^b);
		System.out.println(a<<b);
		System.out.println(a>>b);
		System.out.println(a>>>b);
		// <<< is not valid , >>> this is valid
		
	}
}
