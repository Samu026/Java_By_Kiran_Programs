
public class IncrementDecrement 
{
	public static void main(String args[])
	{
		//Increment and decrement operator 
		
		int a=14;
		System.out.println(++a); // PreIncrement
		System.out.println(a);
		
		System.out.println(a++); // PostIncrement
		System.out.println(a);
		
		System.out.println(--a); // PreDecrement
		System.out.println(a);
		
		System.out.println(a--); //PostDecrement
		System.out.println(a);
		
		int b=14;
		System.out.println(++b); // PreIncrement
		System.out.println(b++); // PostIncrement
		System.out.println(--b); // PreDecrement
		System.out.println(b--); //PostDecrement
	}
}
