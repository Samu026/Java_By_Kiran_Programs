public class AssignmentOperator 
{
	public static void main(String args[])
	{
		int a=3;
		
		a+=5+5;
		System.out.println(a);
		
		a-=3;
		System.out.println(a);
		
		a*=4;
		System.out.println(a);
		
		a/=6;
		System.out.println(a);
		
		a%=3;
		System.out.println(a);
		
		
	}
}
